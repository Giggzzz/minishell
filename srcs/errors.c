#include "minishell.h"

/* ************************************************************************** */
void	err_msg(char *msg)
{
	ft_putendl_fd(msg, 2);
}

/* ************************************************************************** */
void	exit_msg(char *msg)
{
	err_msg(msg);
	exit(1);
}

/* ************************************************************************** */
int manage_perror(char *remark, int error_code)
{
	perror(remark);
	ft_printf("\n"); // a effacer, si besoin
	return (error_code);
}


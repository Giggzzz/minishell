#include "minishell.h"
/* ************************************************************************** */
int pwd_builtin(void)
{
	char	cwd_name[4096];
	
	if (getcwd(cwd_name, 4096) == NULL)	
		return (PWD_ERROR);
	else
	{
		write(1, cwd_name, ft_strlen(cwd_name));
		write(1, "\n", 1);
	}
	
	return (PWD_NO_ERROR);
}

